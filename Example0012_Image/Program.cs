// опишем метод, который будет выводить картинку в массиве pic
void PrintImage(int[,] image)
{
    for(int i=0; i<image.GetLength(0); i++)
    {
        for(int j=0; j<image.GetLength(1); j++)
        {
            if(image[i,j] == 0) Console.Write(" ");
            else Console.Write("*");
        }
        Console.WriteLine();
    }
}
PrintImage(pic);

// Опишем метод, который будет закрашивать картинку
void FillImage(int row, int col)// в качестве аргументов укажем позицию точки
//с которой начнем закрашивать картинку
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row-1, col);// метод вызывает сам себя
        FillImage(row, col-1);
        FillImage(row+1, col);
        FillImage(row, col+1);
    }
}

